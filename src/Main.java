
public class Main {
    public static void main(String[] args) {
        int eded1 = 5;
        int eded2 = 3;
        char emel = '+';

        if (emel == '+')
            System.out.println(eded1 + eded2);
        else if (emel == '-')
            System.out.println(eded1 - eded2);
        else if (emel == '*')
            System.out.println(eded1 * eded2);
        else
            System.out.println(eded1 / eded2);
    }
}